# bai_47 [Mảng độ lệch <= k]:
# Nếu mỗi phần tử cách vị trí đúng tối đa k [cite: 176, 177], vòng lặp chèn bên trong chỉ chạy tối đa k bước cho mỗi phần tử[cite: 178]. 
# Tổng số thao tác dịch chuyển toàn mảng sẽ bị chặn trên bởi O(n * k), chạy nhanh hơn nhiều so với O(n^2) thuần túy[cite: 178, 179].

