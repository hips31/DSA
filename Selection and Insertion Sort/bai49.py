# bai_49 [So sánh 3 thuật toán O(n^2)]:
# - Insertion Sort: Hiệu quả nhất khi mảng nhỏ hoặc gần sắp xếp; thao tác dịch chuyển (shift) rẻ hơn hoán đổi (swap)[cite: 155, 185, 186].
# - Selection Sort: Số so sánh luôn giữ nguyên O(n^2), nhưng tối ưu nhất về ghi nhớ vì số lượng swap cực kỳ thấp (tối đa n-1)[cite: 17, 85, 185].
# - Bubble Sort: Kém hiệu quả nhất do chi phí swap liên tục quá lớn[cite: 185].

